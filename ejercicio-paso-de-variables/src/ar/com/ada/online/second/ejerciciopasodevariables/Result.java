package ar.com.ada.online.second.ejerciciopasodevariables;

public class Result {
    private Integer resultSum;
    private Integer resultSubtraction;
    private Integer resultMultiplication;
    private Integer resultDivision;

    //getters, setters and constructor

    public Integer getResultSum() {
        return this.resultSum;
    }

    public void setResultSum(Integer resultSum) {
        this.resultSum = resultSum;
    }

    public Integer getResultSubstraction() {
        return resultSubtraction;
    }

    public void setResultSubstraction(Integer resultSubstraction) {
        this.resultSubtraction = resultSubstraction;
    }

    public Integer getResultMultiplication() {
        return resultMultiplication;
    }

    public void setResultMultiplication(Integer resultMultiplication) {
        this.resultMultiplication = resultMultiplication;
    }

    public Integer getResultDivision() {
        return resultDivision;
    }

    public void setResultDivision(Integer resultDivision) {
        this.resultDivision = resultDivision;
    }

    //Constructor vacio
    public Result (){}


}


